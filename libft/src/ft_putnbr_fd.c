#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	x;

	if (n == -2147483648)
		write (fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			n = n * -1;
			ft_putchar_fd('-', fd);
		}
		x = n;
		if (n > 9)
		{
			ft_putnbr_fd(x / 10, fd);
			n = n % 10;
		}
		ft_putchar_fd(n + '0', fd);
	}
}
