#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*cc;
	size_t			len;
	int				i;

	i = 0;
	len = 0;
	cc = s;
	while (len < n)
	{
		cc[i] = 0;
		i++;
		len++;
	}
}
