int	ft_lenint(int nb)
{
	int	j;

	j = 1;
	if (nb == -2147483648)
		return (11);
	if (nb < 0)
	{
		nb = nb * -1;
		j++;
	}
	while (nb > 9)
	{
		nb = nb / 10;
		j++;
	}
	return (j);
}
