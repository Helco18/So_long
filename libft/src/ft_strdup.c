#include "libft.h"

char	*ft_strdup(char *src)
{
	int		j;
	int		i;
	char	*dest;

	j = 0;
	while (src[j] != 0)
	{
		j++;
	}
	dest = (char *)malloc(sizeof(char) * j + 1);
	if (!dest)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
